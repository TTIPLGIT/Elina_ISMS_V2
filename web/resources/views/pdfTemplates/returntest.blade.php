<script>
	function countWords(text) {
		return text.split(/\s+/).length;
	}

	document.addEventListener('DOMContentLoaded', function() {
		const table = document.querySelector('table');
		const rows = table.querySelectorAll('tr');

		rows.forEach(row => {
			const cells = row.querySelectorAll('td');
			cells.forEach(cell => {
				const words = countWords(cell.textContent);
				if (words > 500) {
					let contentArray = cell.textContent.split(' ');
					let firstPart = contentArray.slice(0, 500).join(' ');
					let remainingPart = contentArray.slice(500).join(' ');

					cell.textContent = firstPart;

					while (remainingPart) {
						const newRow = document.createElement('tr');
						const newCell = document.createElement('td');

						firstPart = remainingPart.slice(0, 500);
						remainingPart = remainingPart.slice(500);

						newCell.textContent = firstPart;

						newRow.appendChild(document.createElement('td'));
						newRow.appendChild(newCell);
						table.appendChild(newRow);
					}
				}
			});
		});

		var htmlContent = document.documentElement.innerHTML;
		document.getElementById('val').value = htmlContent;
		document.getElementById('submitForm').submit();
	});
</script>


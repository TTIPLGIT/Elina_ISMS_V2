     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        try {
            $method = 'Method => QuadrantQuestionnaireController => index';
            
            $rows = DB::select("SELECT que.status AS questatus, que.*,qud.*,en.* FROM questionnaire_initiation AS que 
            INNER JOIN questionnaire AS qud ON que.questionnaire_id = qud.questionnaire_id
            INNER JOIN enrollment_details AS en on en.enrollment_id=que.enrollment_id 
			WHERE que.activeflag=0 AND qud.questionnaire_type = 'SAIL' AND que.status = 'Submitted'
			AND que.questionnaire_id IN (17 , 20)
			ORDER BY que.questionnaire_initiation_id DESC");

            $serviceResponse = array();
            $serviceResponse['Code'] = config('setting.status_code.success');
            $serviceResponse['Message'] = config('setting.status_message.success');
            $serviceResponse['Data'] = $rows;
            $serviceResponse = json_encode($serviceResponse, JSON_FORCE_OBJECT);
            $sendServiceResponse = $this->SendServiceResponse($serviceResponse, config('setting.status_code.success'), true);
            return $sendServiceResponse;
        } catch (\Exception $exc) {
            $exceptionResponse = array();
            $exceptionResponse['ServiceMethod'] = $method;
            $exceptionResponse['Exception'] = $exc->getMessage();
            $exceptionResponse = json_encode($exceptionResponse, JSON_FORCE_OBJECT);
            $this->WriteFileLog($exceptionResponse);
            $serviceResponse = array();
            $serviceResponse['Code'] = config('setting.status_code.exception');
            $serviceResponse['Message'] = $exc->getMessage();
            $serviceResponse = json_encode($serviceResponse, JSON_FORCE_OBJECT);
            $sendServiceResponse = $this->SendServiceResponse($serviceResponse, config('setting.status_code.exception'), false);
            return $sendServiceResponse;
        }
    }


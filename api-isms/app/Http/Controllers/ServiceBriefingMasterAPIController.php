     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        try {
            $method = 'Method => ServiceBriefingMasterAPIController => update';
            $inputArray = $this->DecryptData($request->requestData);

            $serviceBriefingID = DB::transaction(function () use ($inputArray) {
                if ($inputArray['action'] == 'update') {
                    // $this->WriteFileLog('Update');
                    $this->WriteFileLog($inputArray);
                    DB::table('payment_services_master')
                        ->where('id', $inputArray['id'])
                        ->update([
                            'service_briefing' => $inputArray['option'],
                        ]);
                } else {
                    // $this->WriteFileLog('Remove');
                    // $this->WriteFileLog($inputArray);
                    DB::table('payment_services_master')
                        ->where('id', $inputArray['id'])
                        ->update([
                            'active_flag' => 1,
                        ]);
                }

                return $inputArray['id'];
            });
            $serviceResponse = array();
            $serviceResponse['Code'] = config('setting.status_code.success');
            $serviceResponse['Message'] = config('setting.status_message.success');
            $serviceResponse['Data'] = $serviceBriefingID;
            $serviceResponse = json_encode($serviceResponse, JSON_FORCE_OBJECT);
            $sendServiceResponse = $this->SendServiceResponse($serviceResponse, config('setting.status_code.success'), true);
            return $sendServiceResponse;
        } catch (\Exception $exc) {
            $exceptionResponse = array();
            $exceptionResponse['ServiceMethod'] = $method;
            $exceptionResponse['Exception'] = $exc->getMessage();
            $exceptionResponse = json_encode($exceptionResponse, JSON_FORCE_OBJECT);
            $this->WriteFileLog($exceptionResponse);
            $serviceResponse = array();
